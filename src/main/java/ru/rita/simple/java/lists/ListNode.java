package ru.rita.simple.java.lists;

//https://leetcode.com/problems/merge-two-sorted-lists/

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
